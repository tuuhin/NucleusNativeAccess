package dev.nucleusframework.nna.plugin.codegen

import dev.nucleusframework.nna.plugin.ir.KneClass
import dev.nucleusframework.nna.plugin.ir.KneEnum
import dev.nucleusframework.nna.plugin.ir.KneFunction
import dev.nucleusframework.nna.plugin.ir.KneModule
import dev.nucleusframework.nna.plugin.ir.KneParam
import dev.nucleusframework.nna.plugin.ir.KneProperty
import dev.nucleusframework.nna.plugin.ir.KneType

/**
 * Generates Kotlin/Native bridge code using @CName + StableRef.
 *
 * Inspired by swift-java's Swift @_cdecl thunk generation (SwiftThunkTranslator).
 * Each Kotlin class/function gets a C-compatible symbol via @CName, which the
 * JVM side can call directly through FFM MethodHandles.
 *
 * Object lifecycle mirrors swift-java's approach:
 *   - Constructor → StableRef.create(obj).asCPointer().toLong()  (opaque Long handle)
 *   - Methods     → recover StableRef from handle, call method
 *   - Dispose     → StableRef.dispose()  (equivalent to Swift's swift_release)
 *
 * All bridge functions are wrapped in try/catch. Errors are stored in a
 * @ThreadLocal variable and queried by the JVM side via kne_hasError/kne_getLastError.
 */
class NativeBridgeGenerator {

    /** Whether this type uses the output-buffer pattern for returns. */
    private fun KneType.returnsViaBuffer(): Boolean =
        this == KneType.STRING || this == KneType.BYTE_ARRAY ||
        (this is KneType.NULLABLE && (inner == KneType.STRING || inner == KneType.BYTE_ARRAY))

    /** Whether this type is a collection (List, Set, Map), including nullable. */
    private fun KneType.isCollection(): Boolean = when (this) {
        is KneType.LIST, is KneType.SET, is KneType.MAP -> true
        is KneType.NULLABLE -> inner is KneType.LIST || inner is KneType.SET || inner is KneType.MAP
        else -> false
    }

    /** Extract the inner collection type (unwrap nullable). */
    private fun KneType.unwrapCollection(): KneType = when (this) {
        is KneType.NULLABLE -> inner
        else -> this
    }

    /** Whether this type returns via the collection out-buffer pattern. */
    private fun KneType.returnsViaCollectionBuffer(): Boolean = isCollection()

    fun generate(module: KneModule): String = buildString {
        appendLine("// Auto-generated by kotlin-native-export plugin. Do not modify.")
        appendLine("@file:Suppress(\"all\", \"NOTHING_TO_INLINE\")")
        appendLine("@file:OptIn(kotlinx.cinterop.ExperimentalForeignApi::class, kotlin.experimental.ExperimentalNativeApi::class)")
        appendLine()
        appendLine("import kotlinx.cinterop.*")
        appendLine("import kotlin.concurrent.AtomicReference")
        appendLine("import kotlin.native.concurrent.ObsoleteWorkersApi")

        val hasSuspend = module.classes.any { cls -> cls.methods.any { it.isSuspend } || cls.companionMethods.any { it.isSuspend } } ||
            module.functions.any { it.isSuspend }
        val hasFlow = module.classes.any { cls -> cls.methods.any { it.returnType is KneType.FLOW } } ||
            module.functions.any { it.returnType is KneType.FLOW }
        if (hasSuspend || hasFlow) {
            appendLine("import kotlinx.coroutines.*")
        }
        if (hasFlow) {
            appendLine("import kotlinx.coroutines.flow.*")
        }

        module.packages.forEach { pkg ->
            appendLine("import $pkg.*")
        }
        appendLine()

        // Thread-safe error state using AtomicReference (works with new memory model + foreign threads)
        appendLine("private val _kneLastError = AtomicReference<String?>(null)")
        appendLine()

        appendErrorFunctions(module.libName)

        // Suspend/Flow helper bridges (once per module)
        if (hasSuspend || hasFlow) {
            appendSuspendHelpers(module.libName)
        }

        module.classes.filter { !it.isCommon }.forEach { cls -> appendClass(cls, module.libName) }
        module.enums.forEach { enum -> appendEnum(enum, module.libName) }
        module.functions.filter { !it.isExtension }.forEach { fn -> appendTopLevelFunction(fn, module.libName) }

        // Extension function bridges
        module.functions.filter { it.isExtension && it.receiverType != null }.forEach { fn ->
            appendExtensionFunction(fn, module.libName)
        }

        // Sealed class ordinal bridges
        module.classes.filter { it.isSealed && !it.isCommon }.forEach { cls ->
            appendSealedOrdinalBridge(cls, module.libName)
        }

        // Generate list-of-DC accessor bridges for each DC used in collections
        val dcTypesInLists = collectDcListTypes(module)
        dcTypesInLists.forEach { dc -> appendListDcBridges(dc, module.libName) }

        // Generate DC reader bridges for each DC used as Flow element
        val dcTypesInFlows = collectFlowDcTypes(module)
        dcTypesInFlows.forEach { dc -> appendFlowDcBridges(dc, module.libName) }

        // Generate DC reader bridges for each DC used as suspend return
        val dcTypesInSuspend = collectSuspendDcTypes(module)
        dcTypesInSuspend.forEach { dc -> appendSuspendDcBridges(dc, module.libName) }

        // Generate collection reader bridges for suspend returns + DC collection fields + collection properties + Flow<Collection>
        val suspendCollTypes = collectSuspendCollectionTypes(module)
        val dcFieldCollTypes = collectDcFieldCollectionTypes(module)
        val propCollTypes = collectPropertyCollectionTypes(module)
        val flowCollTypes = collectFlowCollectionTypes(module)
        val allCollReaderTypes = suspendCollTypes + dcFieldCollTypes + propCollTypes + flowCollTypes
        if (allCollReaderTypes.isNotEmpty()) {
            appendSuspendCollectionReaderBridges(module.libName, allCollReaderTypes)
        }

        // Collect collection param types that need wrap bridges (ByteArray/nested collection elements)
        val paramWrapTypes = mutableSetOf<KneType>()
        module.classes.forEach { cls ->
            cls.methods.forEach { fn ->
                fn.params.forEach { p ->
                    val inner = when (val t = p.type) { is KneType.NULLABLE -> t.inner; else -> t }
                    when (inner) {
                        is KneType.LIST -> if (inner.elementType == KneType.BYTE_ARRAY || inner.elementType is KneType.LIST || inner.elementType is KneType.SET || inner.elementType is KneType.MAP) paramWrapTypes.add(KneType.LIST(inner.elementType))
                        is KneType.SET -> if (inner.elementType == KneType.BYTE_ARRAY) paramWrapTypes.add(KneType.SET(inner.elementType))
                        else -> {}
                    }
                }
            }
        }

        // Generate wrap bridges for DC collection fields + mutable collection properties + collection params
        val allWrapTypes = dcFieldCollTypes + propCollTypes + paramWrapTypes
        if (allWrapTypes.isNotEmpty()) {
            appendDcFieldCollectionWrapBridges(module.libName, allWrapTypes)
        }

        // Generate create/add bridges for List<DC> params
        val dcTypesInListParams = collectDcListParamTypes(module)
        dcTypesInListParams.forEach { dc -> appendListDcParamBridges(dc, module.libName) }

        // Generate invoke bridges for function return types
        val fnReturnTypes = collectFunctionReturnTypes(module)
        fnReturnTypes.forEach { fnType -> appendFnInvokeBridge(module.libName, fnType) }
    }

    // ── Error query functions ────────────────────────────────────────────────

    private fun StringBuilder.appendErrorFunctions(prefix: String) {
        appendLine("@CName(\"${prefix}_kne_hasError\")")
        appendLine("fun `${prefix}_kne_hasError`(): Int = if (_kneLastError.value != null) 1 else 0")
        appendLine()
        appendLine("@CName(\"${prefix}_kne_getLastError\")")
        appendLine("fun `${prefix}_kne_getLastError`(outBuf: CPointer<ByteVar>?, outLen: Int): Int {")
        appendLine("    val err = _kneLastError.value ?: return -1")
        appendLine("    _kneLastError.value = null")
        appendLine("    val bytes = err.encodeToByteArray()")
        appendLine("    val writeLen = minOf(bytes.size, outLen - 1)")
        appendLine("    bytes.forEachIndexed { i, b -> if (i < writeLen) outBuf?.set(i, b) }")
        appendLine("    outBuf?.set(writeLen, 0)")
        appendLine("    return bytes.size")
        appendLine("}")
        appendLine()
    }

    // ── Try/catch helpers ────────────────────────────────────────────────────

    private fun StringBuilder.appendTryCatchStart() {
        appendLine("    _kneLastError.value = null")
        appendLine("    try {")
    }

    private fun StringBuilder.appendTryCatchEnd(returnType: KneType) {
        appendLine("    } catch (e: Throwable) {")
        appendLine("        _kneLastError.value = e.message ?: e::class.simpleName ?: \"Unknown error\"")
        if (returnType != KneType.UNIT) {
            appendLine("        return ${defaultErrorValue(returnType)}")
        }
        appendLine("    }")
    }

    private fun defaultErrorValue(type: KneType): String = when (type) {
        KneType.INT -> "0"
        KneType.LONG -> "0L"
        KneType.DOUBLE -> "0.0"
        KneType.FLOAT -> "0.0f"
        KneType.BOOLEAN -> "0"
        KneType.BYTE -> "0"
        KneType.SHORT -> "0"
        KneType.STRING -> "0"
        KneType.UNIT -> ""
        is KneType.OBJECT, is KneType.INTERFACE -> "0L"
        is KneType.ENUM -> "0"
        is KneType.NULLABLE -> when (type.inner) {
            KneType.STRING -> "-1"
            KneType.BOOLEAN, is KneType.ENUM -> "-1"
            KneType.INT, KneType.LONG, KneType.FLOAT, KneType.DOUBLE -> "Long.MIN_VALUE"
            KneType.SHORT, KneType.BYTE -> "Int.MIN_VALUE"
            is KneType.OBJECT, is KneType.INTERFACE -> "0L"
            else -> "0"
        }
        KneType.BYTE_ARRAY -> "0"
        is KneType.FUNCTION -> "0"
        is KneType.DATA_CLASS -> ""
        is KneType.LIST -> "0"
        is KneType.SET -> "0"
        is KneType.MAP -> "0"
        is KneType.FLOW -> ""
    }

    // ── Classes ──────────────────────────────────────────────────────────────

    private fun StringBuilder.appendClass(cls: KneClass, prefix: String) {
        val p = prefix
        val n = cls.simpleName
        val fq = cls.fqName
        val isInstantiable = !cls.isAbstract && !cls.isSealed
        val isRoot = cls.superClass == null

        // Constructor (only for instantiable classes)
        if (isInstantiable) {
            val ctorArgs = buildExpandedParamList(cls.constructor.params)
            appendLine("@CName(\"${p}_${n}_new\")")
            appendLine("fun `${p}_${n}_new`($ctorArgs): Long {")
            appendTryCatchStart()
            val dummyCtorFn = KneFunction("_ctor", cls.constructor.params, KneType.UNIT)
            appendObjectParamConversions(dummyCtorFn)
            val ctorCall = cls.constructor.params.joinToString(", ") { buildCallArg(it.name, it.type) }
            appendLine("    return StableRef.create($fq($ctorCall)).asCPointer().toLong()")
            appendTryCatchEnd(KneType.LONG)
            appendLine("}")
            appendLine()

            // Constructor overloads — drop trailing default params one at a time
            val trailingDefaults = cls.constructor.params.reversed().takeWhile { it.hasDefault }.size
            for (drop in 1..trailingDefaults) {
                val requiredParams = cls.constructor.params.dropLast(drop)
                val suffix = requiredParams.size.toString()
                val oArgs = buildExpandedParamList(requiredParams)
                appendLine("@CName(\"${p}_${n}_new$suffix\")")
                appendLine("fun `${p}_${n}_new$suffix`($oArgs): Long {")
                appendTryCatchStart()
                val dummyFn = KneFunction("_ctor", requiredParams, KneType.UNIT)
                appendObjectParamConversions(dummyFn)
                val oCall = requiredParams.joinToString(", ") { "${it.name} = ${buildCallArg(it.name, it.type)}" }
                appendLine("    return StableRef.create($fq($oCall)).asCPointer().toLong()")
                appendTryCatchEnd(KneType.LONG)
                appendLine("}")
                appendLine()
            }
        }

        // Dispose — every instantiable class gets its own dispose bridge
        if (isInstantiable) {
            appendLine("@CName(\"${p}_${n}_dispose\")")
            appendLine("fun `${p}_${n}_dispose`(handle: Long) {")
            appendLine("    if (handle == 0L) return")
            appendLine("    try {")
            appendLine("        handle.toCPointer<COpaque>()?.asStableRef<$fq>()?.dispose()")
            appendLine("    } catch (_: Throwable) {}")
            appendLine("}")
            appendLine()
        }

        // Methods (dispatch suspend / flow / regular)
        cls.methods.forEach { method ->
            if (method.isSuspend) appendSuspendMethod(method, cls, p)
            else if (method.returnType is KneType.FLOW) appendFlowMethod(method, cls, p)
            else appendMethod(method, cls, p)
        }

        // Properties
        cls.properties.forEach { prop -> appendProperty(prop, cls, p) }

        // Companion methods
        cls.companionMethods.forEach { method -> appendCompanionMethod(method, cls, p) }

        // Companion properties
        cls.companionProperties.forEach { prop -> appendCompanionProperty(prop, cls, p) }
    }

    /** Check if a type is DATA_CLASS or NULLABLE(DATA_CLASS). */
    private fun isDataClassReturn(type: KneType): Boolean =
        type is KneType.DATA_CLASS || (type is KneType.NULLABLE && type.inner is KneType.DATA_CLASS)

    /** Check if a collection return contains DC elements (uses opaque handle instead of flat array). */
    private fun isDcCollectionReturn(type: KneType): Boolean {
        val inner = type.unwrapCollection()
        val elem = when (inner) { is KneType.LIST -> inner.elementType; is KneType.SET -> inner.elementType; else -> null }
        return elem is KneType.DATA_CLASS
    }

    private fun extractDataClass(type: KneType): KneType.DATA_CLASS? = when (type) {
        is KneType.DATA_CLASS -> type
        is KneType.NULLABLE -> type.inner as? KneType.DATA_CLASS
        else -> null
    }

    private fun StringBuilder.appendMethod(fn: KneFunction, cls: KneClass, prefix: String) {
        val symbolName = "${prefix}_${cls.simpleName}_${fn.name}"
        val returnsViaBuffer = fn.returnType.returnsViaBuffer()
        val returnsDataClass = isDataClassReturn(fn.returnType)
        val returnDc = extractDataClass(fn.returnType)
        val returnsNullableDataClass = fn.returnType is KneType.NULLABLE && fn.returnType.inner is KneType.DATA_CLASS
        val returnsCollection = fn.returnType.returnsViaCollectionBuffer()

        // Build param list — expand DATA_CLASS and NULLABLE(DATA_CLASS) params into flat fields
        val extraParams = if (returnsViaBuffer) ", outBuf: CPointer<ByteVar>?, outLen: Int" else ""
        val collectionOutParams = if (returnsCollection) buildCollectionOutParams(fn.returnType) else ""
        val paramList = buildExpandedParamList(fn.params)
        val dcOutParams = if (returnDc != null) buildDataClassOutParams(returnDc) else ""
        val allParams = "handle: Long${if (paramList.isNotEmpty()) ", $paramList" else ""}$extraParams$dcOutParams$collectionOutParams"

        val dcCollection = returnsCollection && isDcCollectionReturn(fn.returnType)
        val returnDecl = when {
            returnsNullableDataClass -> ": Int"
            returnDc != null -> ""
            dcCollection -> ": Long" // opaque handle to List<DC>
            returnsCollection -> ": Int" // element count
            else -> buildReturnDecl(fn.returnType)
        }

        appendLine("@CName(\"$symbolName\")")
        appendLine("fun `$symbolName`($allParams)$returnDecl {")
        appendTryCatchStart()
        appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
        appendObjectParamConversions(fn)
        val callArgs = fn.params.joinToString(", ") { p -> buildCallArg(p.name, p.type) }
        if (returnsNullableDataClass) {
            appendNullableDataClassReturn("obj.${fn.name}($callArgs)", returnDc!!)
        } else if (returnDc != null) {
            appendDataClassReturn("obj.${fn.name}($callArgs)", returnDc)
        } else if (returnsCollection) {
            appendCollectionReturn("obj.${fn.name}($callArgs)", fn.returnType)
        } else {
            appendReturnStatement("obj.${fn.name}($callArgs)", fn.returnType)
        }
        appendTryCatchEnd(if (returnsNullableDataClass) KneType.INT else if (returnDc != null) KneType.UNIT else if (dcCollection) KneType.LONG else if (returnsCollection) KneType.INT else fn.returnType)
        appendLine("}")
        appendLine()
    }

    /** Expand params: DATA_CLASS becomes individual field params (recursive), ByteArray/collections add size param. */
    private fun buildExpandedParamList(params: List<KneParam>): String =
        params.flatMap { p ->
            val dc = extractDataClass(p.type)
            when {
                dc != null -> {
                    val flat = flattenDataClassFields(dc, p.name)
                    val fields = flat.map { (name, type) ->
                        when {
                            type == KneType.STRING -> "$name: CPointer<ByteVar>?"
                            type == KneType.BYTE_ARRAY -> "$name: CPointer<ByteVar>?, ${name}_size: Int"
                            type is KneType.LIST || type is KneType.SET || type is KneType.MAP -> "$name: Long"
                            else -> "$name: ${type.nativeBridgeType}"
                        }
                    }
                    if (p.type is KneType.NULLABLE) listOf("${p.name}_isNull: Int") + fields else fields
                }
                p.type == KneType.BYTE_ARRAY -> listOf("${p.name}: CPointer<ByteVar>?", "${p.name}_size: Int")
                p.type.isCollection() -> {
                    val inner = p.type.unwrapCollection()
                    when (inner) {
                        is KneType.LIST -> if (inner.elementType is KneType.DATA_CLASS) {
                            listOf("${p.name}: Long") // opaque StableRef handle for List<DC>
                        } else {
                            listOf("${p.name}: ${KneType.collectionPointerType(inner.elementType)}", "${p.name}_size: Int")
                        }
                        is KneType.SET -> if (inner.elementType is KneType.DATA_CLASS) {
                            listOf("${p.name}: Long") // opaque StableRef handle for Set<DC>
                        } else {
                            listOf("${p.name}: ${KneType.collectionPointerType(inner.elementType)}", "${p.name}_size: Int")
                        }
                        is KneType.MAP -> listOf(
                            "${p.name}_keys: ${KneType.collectionPointerType(inner.keyType)}",
                            "${p.name}_values: ${KneType.collectionPointerType(inner.valueType)}",
                            "${p.name}_size: Int",
                        )
                        else -> listOf("${p.name}: ${p.type.nativeBridgeType}")
                    }
                }
                else -> listOf("${p.name}: ${p.type.nativeBridgeType}")
            }
        }.joinToString(", ")

    /** Build out-params for DATA_CLASS return (one per field, recursive for nested data class). */
    private fun buildDataClassOutParams(dc: KneType.DATA_CLASS, prefix: String = "out"): String =
        flattenDataClassFields(dc, prefix).joinToString("") { (paramName, type) ->
            when {
                type == KneType.STRING -> ", $paramName: CPointer<ByteVar>?, ${paramName}_len: Int"
                type == KneType.BYTE_ARRAY -> ", $paramName: CPointer<LongVar>?" // StableRef handle
                type is KneType.LIST || type is KneType.SET || type is KneType.MAP ->
                    ", $paramName: CPointer<LongVar>?"
                else -> ", $paramName: CPointer<${type.nativePointerType}>?"
            }
        }

    /** Build a constructor expression from flattened params, recursively handling nested data classes. */
    private fun buildDataClassCtorExpr(dc: KneType.DATA_CLASS, prefix: String): String {
        val fieldArgs = dc.fields.joinToString(", ") { f ->
            val paramName = "${prefix}_${f.name}"
            when (f.type) {
                KneType.BOOLEAN -> "${f.name} = $paramName != 0"
                KneType.STRING -> "${f.name} = $paramName?.toKString() ?: \"\""
                KneType.BYTE_ARRAY -> "${f.name} = ByteArray(${paramName}_size) { $paramName!![it] }"
                is KneType.ENUM -> "${f.name} = ${f.type.fqName}.entries[$paramName]"
                is KneType.OBJECT -> "${f.name} = $paramName.toCPointer<COpaque>()!!.asStableRef<${f.type.fqName}>().get()"
                is KneType.DATA_CLASS -> "${f.name} = ${buildDataClassCtorExpr(f.type, paramName)}"
                is KneType.LIST -> "${f.name} = run { val _ref = $paramName.toCPointer<COpaque>()!!.asStableRef<Any>(); val _v = _ref.get() as List<*>; _ref.dispose(); @Suppress(\"UNCHECKED_CAST\") (_v as List<${f.type.elementType.jvmTypeName}>) }"
                is KneType.SET -> "${f.name} = run { val _ref = $paramName.toCPointer<COpaque>()!!.asStableRef<Any>(); val _v = _ref.get() as Set<*>; _ref.dispose(); @Suppress(\"UNCHECKED_CAST\") (_v as Set<${f.type.elementType.jvmTypeName}>) }"
                is KneType.MAP -> "${f.name} = run { val _ref = $paramName.toCPointer<COpaque>()!!.asStableRef<Any>(); val _v = _ref.get() as Map<*, *>; _ref.dispose(); @Suppress(\"UNCHECKED_CAST\") (_v as Map<${(f.type as KneType.MAP).keyType.jvmTypeName}, ${(f.type as KneType.MAP).valueType.jvmTypeName}>) }"
                else -> "${f.name} = $paramName"
            }
        }
        return "${dc.fqName}($fieldArgs)"
    }

    /** Flatten data class fields, expanding nested data classes with prefixed names. */
    private fun flattenDataClassFields(dc: KneType.DATA_CLASS, prefix: String): List<Pair<String, KneType>> =
        dc.fields.flatMap { f ->
            val name = "${prefix}_${f.name}"
            if (f.type is KneType.DATA_CLASS) flattenDataClassFields(f.type, name)
            else listOf(Pair(name, f.type))
        }

    /** Write data class fields to out-params (recursive for nested data classes). */
    private fun StringBuilder.appendDataClassReturn(expr: String, dc: KneType.DATA_CLASS) {
        appendLine("    val _result = $expr")
        writeDataClassFields("_result", dc, "out")
    }

    private fun StringBuilder.writeDataClassFields(objExpr: String, dc: KneType.DATA_CLASS, prefix: String) {
        dc.fields.forEach { f ->
            val outName = "${prefix}_${f.name}"
            val valueExpr = "$objExpr.${f.name}"
            when (f.type) {
                KneType.STRING -> {
                    appendLine("    val ${outName}_bytes = $valueExpr.encodeToByteArray()")
                    appendLine("    val ${outName}_writeLen = minOf(${outName}_bytes.size, ${outName}_len - 1)")
                    appendLine("    ${outName}_bytes.forEachIndexed { i, b -> if (i < ${outName}_writeLen) $outName?.set(i, b) }")
                    appendLine("    $outName?.set(${outName}_writeLen, 0)")
                }
                KneType.BOOLEAN -> appendLine("    $outName!![0] = if ($valueExpr) 1 else 0")
                is KneType.ENUM -> appendLine("    $outName!![0] = $valueExpr.ordinal")
                is KneType.OBJECT -> appendLine("    $outName!![0] = StableRef.create($valueExpr).asCPointer().toLong()")
                is KneType.DATA_CLASS -> writeDataClassFields(valueExpr, f.type, outName)
                KneType.BYTE_ARRAY ->
                    appendLine("    $outName!![0] = StableRef.create($valueExpr).asCPointer().toLong()")
                is KneType.LIST, is KneType.SET, is KneType.MAP ->
                    appendLine("    $outName!![0] = StableRef.create($valueExpr).asCPointer().toLong()")
                else -> appendLine("    $outName!![0] = $valueExpr")
            }
        }
    }

    /** Write nullable data class fields to out-params. Returns 0 for null, 1 for present. */
    private fun StringBuilder.appendNullableDataClassReturn(expr: String, dc: KneType.DATA_CLASS) {
        appendLine("    val _result = $expr")
        appendLine("    if (_result == null) return 0")
        writeDataClassFields("_result", dc, "out")
        appendLine("    return 1")
    }

    // ── Collection support ────────────────────────────────────────────────────

    /** Build out-params for collection returns (unwraps nullable). */
    private fun buildCollectionOutParams(type: KneType): String {
        val inner = type.unwrapCollection()
        return when (inner) {
            is KneType.LIST -> {
                val elem = inner.elementType
                if (elem is KneType.DATA_CLASS) "" // DC lists use opaque handle, no out-params
                else if (elem == KneType.STRING) ", outBuf: CPointer<ByteVar>?, outBufLen: Int"
                else ", outBuf: ${KneType.collectionPointerType(elem)}, outLen: Int"
            }
            is KneType.SET -> {
                val elem = inner.elementType
                if (elem is KneType.DATA_CLASS) "" // DC sets use opaque handle
                else if (elem == KneType.STRING) ", outBuf: CPointer<ByteVar>?, outBufLen: Int"
                else ", outBuf: ${KneType.collectionPointerType(elem)}, outLen: Int"
            }
            is KneType.MAP -> {
                val k = inner.keyType; val v = inner.valueType
                val keysParam = if (k == KneType.STRING) "outKeys: CPointer<ByteVar>?, outKeysLen: Int" else "outKeys: ${KneType.collectionPointerType(k)}"
                val valuesParam = if (v == KneType.STRING) "outValues: CPointer<ByteVar>?, outValuesLen: Int" else "outValues: ${KneType.collectionPointerType(v)}"
                ", $keysParam, $valuesParam, outLen: Int"
            }
            else -> ""
        }
    }

    /** Emit collection return logic (handles nullable: returns -1 for null). */
    private fun StringBuilder.appendCollectionReturn(expr: String, type: KneType) {
        val isNullable = type is KneType.NULLABLE
        val inner = type.unwrapCollection()
        // List<DataClass> uses opaque handle pattern (returns Long, not flat array)
        val elemType = when (inner) { is KneType.LIST -> inner.elementType; is KneType.SET -> inner.elementType; else -> null }
        if (elemType is KneType.DATA_CLASS) {
            appendLine("    val _result = $expr")
            if (isNullable) appendLine("    if (_result == null) return 0L")
            val listExpr = if (inner is KneType.SET) "_result.toList()" else "_result"
            appendLine("    return StableRef.create($listExpr).asCPointer().toLong()")
            return
        }
        appendLine("    val _result = $expr")
        if (isNullable) {
            appendLine("    if (_result == null) return -1")
        }
        when (inner) {
            is KneType.LIST -> appendCollectionElementsReturn("_result", inner.elementType)
            is KneType.SET -> {
                appendLine("    val _list = _result.toList()")
                appendCollectionElementsReturn("_list", inner.elementType)
            }
            is KneType.MAP -> appendMapReturn(inner)
            else -> appendLine("    return 0")
        }
    }

    private fun StringBuilder.appendCollectionElementsReturn(listExpr: String, elemType: KneType) {
        when (elemType) {
            KneType.STRING -> {
                appendLine("    var _offset = 0")
                appendLine("    for (_s in $listExpr) {")
                appendLine("        val _bytes = _s.encodeToByteArray()")
                appendLine("        if (_offset + _bytes.size + 1 > outBufLen) break")
                appendLine("        _bytes.forEachIndexed { i, b -> outBuf?.set(_offset + i, b) }")
                appendLine("        outBuf?.set(_offset + _bytes.size, 0)")
                appendLine("        _offset += _bytes.size + 1")
                appendLine("    }")
                appendLine("    return $listExpr.size")
            }
            KneType.BOOLEAN -> {
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = if ($listExpr[i]) 1 else 0")
                appendLine("    return $listExpr.size")
            }
            is KneType.ENUM -> {
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = $listExpr[i].ordinal")
                appendLine("    return $listExpr.size")
            }
            is KneType.OBJECT -> {
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create($listExpr[i]).asCPointer().toLong()")
                appendLine("    return $listExpr.size")
            }
            KneType.BYTE_ARRAY -> {
                // ByteArray elements: wrap each as StableRef handle
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create($listExpr[i]).asCPointer().toLong()")
                appendLine("    return $listExpr.size")
            }
            is KneType.LIST, is KneType.SET, is KneType.MAP -> {
                // Nested collection: wrap each inner collection as StableRef handle
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create($listExpr[i] as Any).asCPointer().toLong()")
                appendLine("    return $listExpr.size")
            }
            else -> {
                appendLine("    val _writeLen = minOf($listExpr.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = $listExpr[i]")
                appendLine("    return $listExpr.size")
            }
        }
    }

    private fun StringBuilder.appendMapReturn(type: KneType.MAP) {
        appendLine("    val _keys = _result.keys.toList()")
        appendLine("    val _values = _result.values.toList()")
        appendLine("    val _writeLen = minOf(_result.size, outLen)")
        // Write keys
        appendMapArrayWrite("_keys", type.keyType, "outKeys", if (type.keyType == KneType.STRING) "outKeysLen" else null)
        // Write values
        appendMapArrayWrite("_values", type.valueType, "outValues", if (type.valueType == KneType.STRING) "outValuesLen" else null)
        appendLine("    return _result.size")
    }

    private fun StringBuilder.appendMapArrayWrite(listExpr: String, elemType: KneType, bufName: String, bufLenName: String?) {
        when (elemType) {
            KneType.STRING -> {
                appendLine("    var _${bufName}Offset = 0")
                appendLine("    for (_s in $listExpr) {")
                appendLine("        val _bytes = _s.encodeToByteArray()")
                appendLine("        if (_${bufName}Offset + _bytes.size + 1 > $bufLenName!!) break")
                appendLine("        _bytes.forEachIndexed { i, b -> $bufName?.set(_${bufName}Offset + i, b) }")
                appendLine("        $bufName?.set(_${bufName}Offset + _bytes.size, 0)")
                appendLine("        _${bufName}Offset += _bytes.size + 1")
                appendLine("    }")
            }
            KneType.BOOLEAN -> {
                appendLine("    for (i in 0 until _writeLen) $bufName!![i] = if ($listExpr[i]) 1 else 0")
            }
            is KneType.ENUM -> {
                appendLine("    for (i in 0 until _writeLen) $bufName!![i] = $listExpr[i].ordinal")
            }
            else -> {
                appendLine("    for (i in 0 until _writeLen) $bufName!![i] = $listExpr[i]")
            }
        }
    }

    /** Read a collection from flat native pointer + size into Kotlin collection. */
    private fun buildCollectionParamConversion(paramName: String, type: KneType): String = when (type) {
        is KneType.LIST -> "${paramName}List"
        is KneType.SET -> "${paramName}Set"
        is KneType.MAP -> "${paramName}Map"
        else -> paramName
    }

    private fun StringBuilder.appendProperty(prop: KneProperty, cls: KneClass, prefix: String) {
        val getterName = "${prefix}_${cls.simpleName}_get_${prop.name}"
        val isCollProp = prop.type.isCollection()
        val returnDc = extractDataClass(prop.type)
        val returnsNullableDc = prop.type is KneType.NULLABLE && prop.type.inner is KneType.DATA_CLASS

        if (isCollProp) {
            // Collection property getter: return StableRef handle (Long)
            appendLine("@CName(\"$getterName\")")
            appendLine("fun `$getterName`(handle: Long): Long {")
            appendTryCatchStart()
            appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
            appendLine("    return StableRef.create(obj.${prop.name} as Any).asCPointer().toLong()")
            appendTryCatchEnd(KneType.LONG)
            appendLine("}")
            appendLine()
        } else if (returnDc != null) {
            // Data class property getter: expand fields into out-params
            val dcOutParams = buildDataClassOutParams(returnDc)
            val returnDecl = if (returnsNullableDc) ": Int" else ""
            appendLine("@CName(\"$getterName\")")
            appendLine("fun `$getterName`(handle: Long$dcOutParams)$returnDecl {")
            appendTryCatchStart()
            appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
            if (returnsNullableDc) {
                appendNullableDataClassReturn("obj.${prop.name}", returnDc)
            } else {
                appendDataClassReturn("obj.${prop.name}", returnDc)
            }
            appendTryCatchEnd(if (returnsNullableDc) KneType.INT else KneType.UNIT)
            appendLine("}")
            appendLine()
        } else {
            val extraParams = if (prop.type.returnsViaBuffer()) ", outBuf: CPointer<ByteVar>?, outLen: Int" else ""
            val returnDecl = buildReturnDecl(prop.type)
            appendLine("@CName(\"$getterName\")")
            appendLine("fun `$getterName`(handle: Long$extraParams)$returnDecl {")
            appendTryCatchStart()
            appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
            appendReturnStatement("obj.${prop.name}", prop.type)
            appendTryCatchEnd(prop.type)
            appendLine("}")
            appendLine()
        }

        if (prop.mutable) {
            val setterName = "${prefix}_${cls.simpleName}_set_${prop.name}"
            if (isCollProp) {
                // Collection property setter: accept StableRef handle (Long), read and dispose
                appendLine("@CName(\"$setterName\")")
                appendLine("fun `$setterName`(handle: Long, value: Long) {")
                appendTryCatchStart()
                appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
                appendLine("    val _ref = value.toCPointer<COpaque>()!!.asStableRef<Any>()")
                val inner = prop.type.unwrapCollection()
                when (inner) {
                    is KneType.LIST -> {
                        appendLine("    val _coll = _ref.get() as List<*>")
                        appendLine("    obj.${prop.name} = _coll as List<${inner.elementType.jvmTypeName}>")
                    }
                    is KneType.SET -> {
                        appendLine("    val _coll = _ref.get() as Set<*>")
                        appendLine("    obj.${prop.name} = _coll as Set<${inner.elementType.jvmTypeName}>")
                    }
                    is KneType.MAP -> {
                        appendLine("    val _coll = _ref.get() as Map<*, *>")
                        appendLine("    obj.${prop.name} = _coll as Map<${(inner as KneType.MAP).keyType.jvmTypeName}, ${inner.valueType.jvmTypeName}>")
                    }
                    else -> {}
                }
                appendLine("    _ref.dispose()")
                appendTryCatchEnd(KneType.UNIT)
                appendLine("}")
                appendLine()
            } else {
                val valueParam = "value: ${prop.type.nativeBridgeType}"
                appendLine("@CName(\"$setterName\")")
                appendLine("fun `$setterName`(handle: Long, $valueParam) {")
                appendTryCatchStart()
                appendLine("    val obj = handle.toCPointer<COpaque>()!!.asStableRef<${cls.fqName}>().get()")
                appendSetterBody("obj.${prop.name}", prop.type)
                appendTryCatchEnd(KneType.UNIT)
                appendLine("}")
                appendLine()
            }
        }
    }

