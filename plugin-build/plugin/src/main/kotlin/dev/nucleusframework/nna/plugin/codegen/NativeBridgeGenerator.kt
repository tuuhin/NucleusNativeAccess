    /** Build the return type declaration for a bridge function. */
    private fun buildReturnDecl(type: KneType): String = when {
        type.returnsViaBuffer() -> ": Int" // byte count (or -1 for null)
        type.returnsViaCollectionBuffer() -> ": Int" // element count
        type == KneType.UNIT -> ""
        type is KneType.OBJECT -> ": Long"
        type is KneType.INTERFACE -> ": Long"
        type is KneType.ENUM -> ": Int"
        type is KneType.NULLABLE -> ": ${type.nativeBridgeType}"
        else -> ": ${type.nativeBridgeType}"
    }

    /** Convert a parameter value at the call site. */
    private fun buildParamConversion(paramName: String, type: KneType): String = when {
        type == KneType.BOOLEAN -> "$paramName != 0"
        type == KneType.STRING -> "$paramName?.toKString() ?: \"\""
        type == KneType.BYTE_ARRAY -> "${paramName}Arr"
        type is KneType.LIST -> "${paramName}List"
        type is KneType.SET -> "${paramName}Set"
        type is KneType.MAP -> "${paramName}Map"
        type is KneType.NULLABLE && type.inner is KneType.LIST -> "${paramName}List"
        type is KneType.NULLABLE && type.inner is KneType.SET -> "${paramName}Set"
        type is KneType.NULLABLE && type.inner is KneType.MAP -> "${paramName}Map"
        type is KneType.OBJECT -> "${paramName}Obj"
        type is KneType.INTERFACE -> "${paramName}Obj"
        type is KneType.ENUM -> "${type.fqName}.entries[$paramName]"
        type is KneType.FUNCTION -> "${paramName}Fn"
        type is KneType.DATA_CLASS -> "${paramName}Obj"
        type is KneType.NULLABLE && type.inner is KneType.DATA_CLASS -> "${paramName}Obj"
        type is KneType.NULLABLE -> buildNullableParamConversion(paramName, type)
        else -> paramName
    }

    private fun buildNullableParamConversion(paramName: String, type: KneType.NULLABLE): String = when (type.inner) {
        KneType.STRING -> "$paramName?.toKString()"
        KneType.BOOLEAN -> "if ($paramName >= 0) ($paramName != 0) else null"
        KneType.INT -> "if ($paramName != Long.MIN_VALUE) $paramName.toInt() else null"
        KneType.LONG -> "if ($paramName != Long.MIN_VALUE) $paramName else null"
        KneType.SHORT -> "if ($paramName != Int.MIN_VALUE) $paramName.toShort() else null"
        KneType.BYTE -> "if ($paramName != Int.MIN_VALUE) $paramName.toByte() else null"
        KneType.FLOAT -> "if ($paramName != Long.MIN_VALUE) Float.fromBits($paramName.toInt()) else null"
        KneType.DOUBLE -> "if ($paramName != Long.MIN_VALUE) Double.fromBits($paramName) else null"
        is KneType.OBJECT -> "if ($paramName != 0L) $paramName.toCPointer<COpaque>()!!.asStableRef<${type.inner.fqName}>().get() else null"
        is KneType.INTERFACE -> "if ($paramName != 0L) $paramName.toCPointer<COpaque>()!!.asStableRef<${type.inner.fqName}>().get() else null"
        is KneType.ENUM -> "if ($paramName >= 0) ${type.inner.fqName}.entries[$paramName] else null"
        else -> paramName
    }

    /** Build the call argument expression for a parameter. */
    private fun buildCallArg(paramName: String, type: KneType): String = when {
        type == KneType.STRING -> "${paramName}Str"
        type == KneType.BYTE_ARRAY -> "${paramName}Arr"
        type == KneType.BOOLEAN -> "$paramName != 0"
        type is KneType.LIST -> "${paramName}List"
        type is KneType.SET -> "${paramName}Set"
        type is KneType.MAP -> "${paramName}Map"
        type is KneType.NULLABLE && type.inner is KneType.LIST -> "${paramName}List"
        type is KneType.NULLABLE && type.inner is KneType.SET -> "${paramName}Set"
        type is KneType.NULLABLE && type.inner is KneType.MAP -> "${paramName}Map"
        type is KneType.OBJECT -> "${paramName}Obj"
        type is KneType.ENUM -> "${type.fqName}.entries[$paramName]"
        type is KneType.FUNCTION -> "${paramName}Fn"
        type is KneType.NULLABLE && type.inner is KneType.FUNCTION -> "${paramName}NullFn"
        type is KneType.DATA_CLASS -> "${paramName}Obj"
        type is KneType.NULLABLE && type.inner is KneType.DATA_CLASS -> "${paramName}Obj"
        type is KneType.NULLABLE -> buildNullableCallArg(paramName, type)
        else -> paramName
    }

    private fun buildNullableCallArg(paramName: String, type: KneType.NULLABLE): String = when (type.inner) {
        KneType.STRING -> "${paramName}Str"
        is KneType.OBJECT -> "${paramName}Obj"
        KneType.BOOLEAN -> "if ($paramName >= 0) ($paramName != 0) else null"
        KneType.INT -> "if ($paramName != Long.MIN_VALUE) $paramName.toInt() else null"
        KneType.LONG -> "if ($paramName != Long.MIN_VALUE) $paramName else null"
        KneType.SHORT -> "if ($paramName != Int.MIN_VALUE) $paramName.toShort() else null"
        KneType.BYTE -> "if ($paramName != Int.MIN_VALUE) $paramName.toByte() else null"
        KneType.FLOAT -> "if ($paramName != Long.MIN_VALUE) Float.fromBits($paramName.toInt()) else null"
        KneType.DOUBLE -> "if ($paramName != Long.MIN_VALUE) Double.fromBits($paramName) else null"
        is KneType.ENUM -> "if ($paramName >= 0) ${type.inner.fqName}.entries[$paramName] else null"
        else -> paramName
    }

    /** Emit local variable conversions for String, ByteArray, Object, and Function params (including nullable variants). */
    private fun StringBuilder.appendObjectParamConversions(fn: KneFunction) {
        fn.params.filter { it.type == KneType.STRING }.forEach { p ->
            appendLine("    val ${p.name}Str = ${p.name}?.toKString() ?: \"\"")
        }
        fn.params.filter { it.type == KneType.BYTE_ARRAY }.forEach { p ->
            appendLine("    val ${p.name}Arr = ByteArray(${p.name}_size) { ${p.name}!![it] }")
        }
        fn.params.filter { it.type is KneType.OBJECT }.forEach { p ->
            val objType = p.type as KneType.OBJECT
            appendLine("    val ${p.name}Obj = ${p.name}.toCPointer<COpaque>()!!.asStableRef<${objType.fqName}>().get()")
        }
        fn.params.filter { it.type is KneType.NULLABLE && (it.type as KneType.NULLABLE).inner == KneType.STRING }.forEach { p ->
            appendLine("    val ${p.name}Str = ${p.name}?.toKString()")
        }
        fn.params.filter { it.type is KneType.NULLABLE && (it.type as KneType.NULLABLE).inner is KneType.OBJECT }.forEach { p ->
            val objType = (p.type as KneType.NULLABLE).inner as KneType.OBJECT
            appendLine("    val ${p.name}Obj = if (${p.name} != 0L) ${p.name}.toCPointer<COpaque>()!!.asStableRef<${objType.fqName}>().get() else null")
        }
        fn.params.filter { it.type is KneType.FUNCTION }.forEach { p ->
            val fnType = p.type as KneType.FUNCTION
            appendFunctionParamConversion(p.name, fnType)
        }
        // Nullable function params: generate lambda conversion, wrap as nullable
        fn.params.filter { it.type is KneType.NULLABLE && (it.type as KneType.NULLABLE).inner is KneType.FUNCTION }.forEach { p ->
            val fnType = (p.type as KneType.NULLABLE).inner as KneType.FUNCTION
            // Generate the conversion as if non-null (creates ${p.name}Fn variable)
            appendFunctionParamConversion(p.name, fnType)
            // Then create the nullable wrapper
            appendLine("    val ${p.name}NullFn = if (${p.name} == 0L) null else ${p.name}Fn")
        }
        // Reconstruct data classes from flattened fields (including nullable variants and nested)
        fn.params.forEach { p ->
            val dc = extractDataClass(p.type) ?: return@forEach
            val ctorExpr = buildDataClassCtorExpr(dc, p.name)
            if (p.type is KneType.NULLABLE) {
                appendLine("    val ${p.name}Obj = if (${p.name}_isNull != 0) null else $ctorExpr")
            } else {
                appendLine("    val ${p.name}Obj = $ctorExpr")
            }
        }
        // Reconstruct collections from flat native pointers (including nullable)
        fn.params.forEach { p ->
            val isNullable = p.type is KneType.NULLABLE
            val inner = if (isNullable) (p.type as KneType.NULLABLE).inner else p.type
            when (inner) {
                is KneType.LIST -> {
                    if (inner.elementType is KneType.DATA_CLASS) {
                        val dcFq = (inner.elementType as KneType.DATA_CLASS).fqName
                        if (isNullable) {
                            appendLine("    val ${p.name}List: List<$dcFq>? = if (${p.name} == 0L) null else {")
                            appendLine("        val _ref = ${p.name}.toCPointer<COpaque>()!!.asStableRef<MutableList<$dcFq>>()")
                            appendLine("        val _tmp = _ref.get().toList(); _ref.dispose(); _tmp")
                            appendLine("    }")
                        } else {
                            appendLine("    val _${p.name}Ref = ${p.name}.toCPointer<COpaque>()!!.asStableRef<MutableList<$dcFq>>()")
                            appendLine("    val ${p.name}List = _${p.name}Ref.get().toList()")
                            appendLine("    _${p.name}Ref.dispose()")
                        }
                    } else if (isNullable) {
                        appendLine("    val ${p.name}List = if (${p.name}_size < 0) null else {")
                        appendCollectionParamReadInner(p.name, inner.elementType, "List")
                        appendLine("    }")
                    } else appendCollectionParamRead(p.name, inner.elementType, "List")
                }
                is KneType.SET -> {
                    if (inner.elementType is KneType.DATA_CLASS) {
                        val dcFq = (inner.elementType as KneType.DATA_CLASS).fqName
                        if (isNullable) {
                            appendLine("    val ${p.name}Set: Set<$dcFq>? = if (${p.name} == 0L) null else {")
                            appendLine("        val _ref = ${p.name}.toCPointer<COpaque>()!!.asStableRef<MutableList<$dcFq>>()")
                            appendLine("        val _tmp = _ref.get().toSet(); _ref.dispose(); _tmp")
                            appendLine("    }")
                        } else {
                            appendLine("    val _${p.name}Ref = ${p.name}.toCPointer<COpaque>()!!.asStableRef<MutableList<$dcFq>>()")
                            appendLine("    val ${p.name}Set = _${p.name}Ref.get().toSet()")
                            appendLine("    _${p.name}Ref.dispose()")
                        }
                    } else if (isNullable) {
                        appendLine("    val ${p.name}Set = if (${p.name}_size < 0) null else {")
                        appendCollectionParamReadInner(p.name, inner.elementType, "List")
                        appendLine("    .toSet() }")
                    } else appendCollectionParamRead(p.name, inner.elementType, "Set")
                }
                is KneType.MAP -> {
                    if (isNullable) {
                        appendLine("    val ${p.name}Map: Map<${inner.keyType.jvmTypeName}, ${inner.valueType.jvmTypeName}>? = if (${p.name}_size < 0) null else {")
                        appendMapParamReadInner(p.name, inner)
                        appendLine("    }")
                    } else appendMapParamRead(p.name, inner)
                }
                else -> {}
            }
        }
    }

    /** Emit local variable to read a List/Set from native pointer + size. */
    private fun StringBuilder.appendCollectionParamRead(paramName: String, elemType: KneType, collType: String) {
        // Always build as List first, then convert to Set if needed
        val listVar = "${paramName}List"
        when (elemType) {
            KneType.STRING -> {
                appendLine("    val $listVar = mutableListOf<String>()")
                appendLine("    var _${paramName}Offset = 0")
                appendLine("    for (i in 0 until ${paramName}_size) {")
                appendLine("        val _s = ($paramName!! + _${paramName}Offset)!!.toKString()")
                appendLine("        $listVar.add(_s)")
                appendLine("        _${paramName}Offset += _s.encodeToByteArray().size + 1")
                appendLine("    }")
            }
            KneType.BOOLEAN -> {
                appendLine("    val $listVar = List(${paramName}_size) { $paramName!![it] != 0 }")
            }
            is KneType.ENUM -> {
                appendLine("    val $listVar = List(${paramName}_size) { ${elemType.fqName}.entries[$paramName!![it]] }")
            }
            is KneType.OBJECT, is KneType.INTERFACE -> {
                val fqn = when (elemType) { is KneType.OBJECT -> elemType.fqName; is KneType.INTERFACE -> elemType.fqName; else -> error("unreachable") }
                appendLine("    val $listVar = List(${paramName}_size) { $paramName!![it].toCPointer<COpaque>()!!.asStableRef<$fqn>().get() }")
            }
            KneType.BYTE_ARRAY -> {
                // ByteArray elements: each is a StableRef handle
                appendLine("    val $listVar = List(${paramName}_size) {")
                appendLine("        val _ref = $paramName!![it].toCPointer<COpaque>()!!.asStableRef<ByteArray>()")
                appendLine("        val _ba = _ref.get(); _ref.dispose(); _ba")
                appendLine("    }")
            }
            is KneType.LIST, is KneType.SET, is KneType.MAP -> {
                // Nested collection: each element is a StableRef handle, cast to correct type
                appendLine("    val $listVar: List<${elemType.jvmTypeName}> = List(${paramName}_size) {")
                appendLine("        val _innerRef = $paramName!![it].toCPointer<COpaque>()!!.asStableRef<Any>()")
                appendLine("        val _inner = _innerRef.get(); _innerRef.dispose(); _inner as ${elemType.jvmTypeName}")
                appendLine("    }")
            }
            else -> {
                appendLine("    val $listVar = List(${paramName}_size) { $paramName!![it] }")
            }
        }
        if (collType == "Set") {
            appendLine("    val ${paramName}Set = $listVar.toSet()")
        }
    }

    /** Emit an expression-returning collection read (for nullable wrappers). */
    private fun StringBuilder.appendCollectionParamReadInner(paramName: String, elemType: KneType, collType: String) {
        when (elemType) {
            KneType.STRING -> {
                appendLine("        val _${paramName}Tmp = mutableListOf<String>()")
                appendLine("        var _${paramName}Offset = 0")
                appendLine("        for (i in 0 until ${paramName}_size) {")
                appendLine("            val _s = ($paramName!! + _${paramName}Offset)!!.toKString()")
                appendLine("            _${paramName}Tmp.add(_s)")
                appendLine("            _${paramName}Offset += _s.encodeToByteArray().size + 1")
                appendLine("        }")
                appendLine("        _${paramName}Tmp")
            }
            KneType.BOOLEAN -> appendLine("        List(${paramName}_size) { $paramName!![it] != 0 }")
            is KneType.ENUM -> appendLine("        List(${paramName}_size) { ${elemType.fqName}.entries[$paramName!![it]] }")
            is KneType.OBJECT -> appendLine("        List(${paramName}_size) { $paramName!![it].toCPointer<COpaque>()!!.asStableRef<${elemType.fqName}>().get() }")
            is KneType.INTERFACE -> appendLine("        List(${paramName}_size) { $paramName!![it].toCPointer<COpaque>()!!.asStableRef<${elemType.fqName}>().get() }")
            else -> appendLine("        List(${paramName}_size) { $paramName!![it] }")
        }
    }

    /** Emit an expression-returning map read (for nullable wrappers). */
    private fun StringBuilder.appendMapParamReadInner(paramName: String, type: KneType.MAP) {
        // Reuse the same logic but as expression
        appendMapParamRead(paramName, type)
        appendLine("        ${paramName}Map")
    }

    /** Emit local variable to read a Map from native parallel arrays. */
    private fun StringBuilder.appendMapParamRead(paramName: String, type: KneType.MAP) {
        // Read keys
        if (type.keyType == KneType.STRING) {
            appendLine("    val _${paramName}Keys = mutableListOf<String>()")
            appendLine("    var _${paramName}KeysOff = 0")
            appendLine("    for (i in 0 until ${paramName}_size) {")
            appendLine("        val _s = (${paramName}_keys!! + _${paramName}KeysOff)!!.toKString()")
            appendLine("        _${paramName}Keys.add(_s)")
            appendLine("        _${paramName}KeysOff += _s.encodeToByteArray().size + 1")
            appendLine("    }")
        } else {
            appendLine("    val _${paramName}Keys = List(${paramName}_size) { ${buildCollectionElementRead("${paramName}_keys", type.keyType, "it")} }")
        }
        // Read values
        if (type.valueType == KneType.STRING) {
            appendLine("    val _${paramName}Values = mutableListOf<String>()")
            appendLine("    var _${paramName}ValuesOff = 0")
            appendLine("    for (i in 0 until ${paramName}_size) {")
            appendLine("        val _s = (${paramName}_values!! + _${paramName}ValuesOff)!!.toKString()")
            appendLine("        _${paramName}Values.add(_s)")
            appendLine("        _${paramName}ValuesOff += _s.encodeToByteArray().size + 1")
            appendLine("    }")
        } else {
            appendLine("    val _${paramName}Values = List(${paramName}_size) { ${buildCollectionElementRead("${paramName}_values", type.valueType, "it")} }")
        }
        appendLine("    val ${paramName}Map = _${paramName}Keys.zip(_${paramName}Values).toMap()")
    }

    /** Build an expression to read one element from a native array at given index. */
    private fun buildCollectionElementRead(ptrName: String, elemType: KneType, indexExpr: String): String = when (elemType) {
        KneType.BOOLEAN -> "$ptrName!![$indexExpr] != 0"
        is KneType.ENUM -> "${elemType.fqName}.entries[$ptrName!![$indexExpr]]"
        is KneType.OBJECT -> "$ptrName!![$indexExpr].toCPointer<COpaque>()!!.asStableRef<${elemType.fqName}>().get()"
        else -> "$ptrName!![$indexExpr]"
    }

    /** Generate a Kotlin lambda wrapper that calls through the native CFunction pointer. */
    private fun StringBuilder.appendFunctionParamConversion(paramName: String, fnType: KneType.FUNCTION) {
        // Expand DATA_CLASS params into individual fields at C ABI level
        val cFuncParams = fnType.paramTypes.flatMap { expandCFuncParamTypes(it) }.joinToString(", ")
        val cFuncReturn = cFunctionReturnType(fnType.returnType)
        val cFuncType = "CFunction<($cFuncParams) -> $cFuncReturn>"

        val lambdaParams = fnType.paramTypes.mapIndexed { i, t -> "_p$i: ${t.jvmTypeName}" }
        val lambdaParamsStr = lambdaParams.joinToString(", ")
        val hasStringInvoke = fnType.paramTypes.any {
            it == KneType.STRING || it == KneType.BYTE_ARRAY || (it is KneType.DATA_CLASS && it.fields.any { f -> f.type == KneType.STRING })
        }
        val hasCollectionInvoke = fnType.paramTypes.any { it is KneType.LIST || it is KneType.SET || it is KneType.MAP }
        val needsMemScoped = hasStringInvoke || hasCollectionInvoke

        appendLine("    val ${paramName}Fn: ${fnType.jvmTypeName} = { $lambdaParamsStr ->")
        if (needsMemScoped) {
            appendLine("        memScoped {")
        }
        appendLine("        val _fnPtr = ${paramName}.toCPointer<$cFuncType>()!!")

        // Pre-allocate collection arrays for CFunction invoke (including Map)
        fnType.paramTypes.forEachIndexed { i, t ->
            if (t is KneType.MAP) {
                appendLine("        val _keys$i = _p$i.keys.toList()")
                appendLine("        val _values$i = _p$i.values.toList()")
                appendCollectionArrayAlloc(i, t.keyType, "_keys$i", "k")
                appendCollectionArrayAlloc(i, t.valueType, "_values$i", "v")
            }
            if (t is KneType.LIST || t is KneType.SET) {
                val elemType = if (t is KneType.LIST) t.elementType else (t as KneType.SET).elementType
                val src = if (t is KneType.SET) "_p$i.toList()" else "_p$i"
                val varType = KneType.collectionElementVarType(elemType)
                when (elemType) {
                    KneType.STRING -> {
                        appendLine("        val _coll$i = $src")
                        appendLine("        val _buf$i = allocArray<ByteVar>(_coll$i.sumOf { it.encodeToByteArray().size + 1 }.coerceAtLeast(1))")
                        appendLine("        var _off$i = 0")
                        appendLine("        for (_s in _coll$i) { val _b = _s.encodeToByteArray(); _b.forEachIndexed { j, b -> _buf${i}[_off$i + j] = b }; _buf${i}[_off$i + _b.size] = 0; _off$i += _b.size + 1 }")
                    }
                    KneType.BOOLEAN -> {
                        appendLine("        val _coll$i = $src")
                        appendLine("        val _buf$i = allocArray<IntVar>(_coll$i.size)")
                        appendLine("        _coll$i.forEachIndexed { j, v -> _buf${i}[j] = if (v) 1 else 0 }")
                    }
                    is KneType.ENUM -> {
                        appendLine("        val _coll$i = $src")
                        appendLine("        val _buf$i = allocArray<IntVar>(_coll$i.size)")
                        appendLine("        _coll$i.forEachIndexed { j, v -> _buf${i}[j] = v.ordinal }")
                    }
                    else -> {
                        appendLine("        val _coll$i = $src")
                        appendLine("        val _buf$i = allocArray<$varType>(_coll$i.size)")
                        appendLine("        _coll$i.forEachIndexed { j, v -> _buf${i}[j] = v }")
                    }
                }
            }
        }

        // Build invoke args — decompose DATA_CLASS into fields, convert enum to ordinal, expand collections to ptr+size
        // Pre-allocate ByteArray buffers for CFunction invoke
        fnType.paramTypes.forEachIndexed { i, t ->
            if (t == KneType.BYTE_ARRAY) {
                appendLine("        val _baBuf$i = allocArray<ByteVar>(_p$i.size)")
                appendLine("        _p$i.forEachIndexed { j, b -> _baBuf${i}[j] = b }")
            }
        }

        val invokeArgs = fnType.paramTypes.mapIndexed { i, t ->
            when (t) {
                KneType.BOOLEAN -> "if (_p$i) 1 else 0"
                KneType.STRING -> "_p$i.cstr.ptr"
                KneType.BYTE_ARRAY -> "_baBuf$i, _p$i.size"
                is KneType.ENUM -> "_p$i.ordinal"
                is KneType.OBJECT -> "StableRef.create(_p$i).asCPointer().toLong()"
                is KneType.DATA_CLASS -> t.fields.joinToString(", ") { f ->
                    when (f.type) {
                        KneType.BOOLEAN -> "if (_p$i.${f.name}) 1 else 0"
                        KneType.STRING -> "_p$i.${f.name}.cstr.ptr"
                        else -> "_p$i.${f.name}"
                    }
                }
                is KneType.LIST, is KneType.SET -> "_buf$i, _coll$i.size"
                is KneType.MAP -> "_kBuf$i, _vBuf$i, _p$i.size"
                else -> "_p$i"
            }
        }.joinToString(", ")

        if (fnType.returnType == KneType.UNIT) {
            appendLine("        _fnPtr.invoke($invokeArgs)")
        } else if (fnType.returnType == KneType.BYTE_ARRAY) {
            // Callback returns ByteArray as packed buffer: [size:Int32][pad:4][data...]
            appendLine("        val _retPtr = _fnPtr.invoke($invokeArgs)!!")
            appendLine("        val _baSize = _retPtr.reinterpret<IntVar>().pointed.value")
            appendLine("        ByteArray(_baSize) { (_retPtr + 8 + it)!!.pointed.value }")
        } else if (fnType.returnType == KneType.BOOLEAN) {
            appendLine("        _fnPtr.invoke($invokeArgs) != 0")
        } else if (fnType.returnType == KneType.STRING) {
            appendLine("        _fnPtr.invoke($invokeArgs)?.toKString() ?: \"\"")
        } else if (fnType.returnType is KneType.ENUM) {
            appendLine("        ${fnType.returnType.fqName}.entries[_fnPtr.invoke($invokeArgs)]")
        } else if (fnType.returnType is KneType.OBJECT) {
            appendLine("        _fnPtr.invoke($invokeArgs).toCPointer<COpaque>()!!.asStableRef<${fnType.returnType.fqName}>().get()")
        } else if (fnType.returnType is KneType.DATA_CLASS) {
            val dc = fnType.returnType as KneType.DATA_CLASS
            appendLine("        val _retPtr = _fnPtr.invoke($invokeArgs)!!")
            var offset = 0
            val fieldArgs = dc.fields.joinToString(", ") { f ->
                val expr = when (f.type) {
                    KneType.INT -> { val r = "(_retPtr + $offset)!!.reinterpret<IntVar>().pointed.value"; offset += 4; r }
                    KneType.LONG -> { val r = "(_retPtr + $offset)!!.reinterpret<LongVar>().pointed.value"; offset += 8; r }
                    KneType.DOUBLE -> { val r = "(_retPtr + $offset)!!.reinterpret<DoubleVar>().pointed.value"; offset += 8; r }
                    KneType.FLOAT -> { val r = "(_retPtr + $offset)!!.reinterpret<FloatVar>().pointed.value"; offset += 4; r }
                    KneType.BOOLEAN -> { val r = "(_retPtr + $offset)!!.reinterpret<IntVar>().pointed.value != 0"; offset += 4; r }
                    KneType.SHORT -> { val r = "(_retPtr + $offset)!!.reinterpret<ShortVar>().pointed.value"; offset += 2; r }
                    KneType.BYTE -> { val r = "(_retPtr + $offset)!!.reinterpret<ByteVar>().pointed.value"; offset += 1; r }
                    KneType.STRING -> { val r = "(_retPtr + $offset)!!.reinterpret<CPointerVar<ByteVar>>().pointed.value?.toKString() ?: \"\""; offset += 8; r }
                    else -> { offset += 8; "0" }
                }
                "${f.name} = $expr"
            }
            appendLine("        ${dc.fqName}($fieldArgs)")
        } else if (fnType.returnType is KneType.LIST || fnType.returnType is KneType.SET) {
            val elemType = when (fnType.returnType) { is KneType.LIST -> fnType.returnType.elementType; is KneType.SET -> (fnType.returnType as KneType.SET).elementType; else -> KneType.INT }
            appendLine("        val _retPtr = _fnPtr.invoke($invokeArgs)!!")
            appendLine("        val _count = _retPtr.reinterpret<IntVar>().pointed.value")
            appendCollectionReturnFromPackedBuffer(elemType, fnType.returnType is KneType.SET)
        } else if (fnType.returnType is KneType.MAP) {
            val mapType = fnType.returnType as KneType.MAP
            appendLine("        val _retPtr = _fnPtr.invoke($invokeArgs)!!")
            appendLine("        val _count = _retPtr.reinterpret<IntVar>().pointed.value")
            appendMapReturnFromPackedBuffer(mapType)
        } else {
            appendLine("        _fnPtr.invoke($invokeArgs)")
        }
        if (needsMemScoped) {
            appendLine("        }")
        }
        appendLine("    }")
    }

    /** Read a collection from packed buffer returned by callback. Buffer: [count:Int32][4-byte pad][ elements...] */
    private fun StringBuilder.appendCollectionReturnFromPackedBuffer(elemType: KneType, isSet: Boolean) {
        val H = 8 // 8-byte header: count(4) + padding(4) for alignment
        val varType = KneType.collectionElementVarType(elemType)
        when (elemType) {
            KneType.STRING -> {
                appendLine("        val _list = mutableListOf<String>()")
                appendLine("        var _sOff = $H")
                appendLine("        for (i in 0 until _count) {")
                appendLine("            val _s = (_retPtr + _sOff)!!.toKString()")
                appendLine("            _list.add(_s)")
                appendLine("            _sOff += _s.encodeToByteArray().size + 1")
                appendLine("        }")
            }
            KneType.BOOLEAN -> {
                appendLine("        val _dataPtr = (_retPtr + $H)!!.reinterpret<IntVar>()")
                appendLine("        val _list = List(_count) { _dataPtr[it] != 0 }")
            }
            is KneType.ENUM -> {
                appendLine("        val _dataPtr = (_retPtr + $H)!!.reinterpret<IntVar>()")
                appendLine("        val _list = List(_count) { ${elemType.fqName}.entries[_dataPtr[it]] }")
            }
            else -> {
                appendLine("        val _dataPtr = (_retPtr + $H)!!.reinterpret<$varType>()")
                appendLine("        val _list = List(_count) { _dataPtr[it] }")
            }
        }
        if (isSet) appendLine("        _list.toSet()")
        else appendLine("        _list")
    }

    /** Read a map from packed buffer returned by callback. Buffer: [count:Int32][4-pad][keys...][ values...] */
    private fun StringBuilder.appendMapReturnFromPackedBuffer(mapType: KneType.MAP) {
        val H = 8 // 8-byte header for alignment
        val kSize = if (mapType.keyType == KneType.STRING) 0 else fieldSize(mapType.keyType)
        val vSize = if (mapType.valueType == KneType.STRING) 0 else fieldSize(mapType.valueType)
        appendReadPackedArrayFromBuffer("_keys", mapType.keyType, "$H")
        val valOffset = if (kSize > 0) {
            "$H + _count * $kSize"
        } else if (vSize > 1) {
            "(_keysEndOff + ${vSize - 1}) / $vSize * $vSize" // align
        } else {
            "_keysEndOff"
        }
        appendReadPackedArrayFromBuffer("_values", mapType.valueType, valOffset)
        appendLine("        _keys.zip(_values).toMap()")
    }

    private fun StringBuilder.appendReadPackedArrayFromBuffer(varName: String, elemType: KneType, startOffset: String) {
        when (elemType) {
            KneType.STRING -> {
                appendLine("        val $varName = mutableListOf<String>()")
                appendLine("        var ${varName}Off = $startOffset")
                appendLine("        for (i in 0 until _count) {")
                appendLine("            val _s = (_retPtr + ${varName}Off)!!.toKString()")
                appendLine("            $varName.add(_s)")
                appendLine("            ${varName}Off += _s.encodeToByteArray().size + 1")
                appendLine("        }")
                appendLine("        val ${varName}EndOff = ${varName}Off")
            }
            KneType.BOOLEAN -> {
                appendLine("        val ${varName}Ptr = (_retPtr + $startOffset)!!.reinterpret<IntVar>()")
                appendLine("        val $varName = List(_count) { ${varName}Ptr[it] != 0 }")
            }
            is KneType.ENUM -> {
                appendLine("        val ${varName}Ptr = (_retPtr + $startOffset)!!.reinterpret<IntVar>()")
                appendLine("        val $varName = List(_count) { ${elemType.fqName}.entries[${varName}Ptr[it]] }")
            }
            else -> {
                val varType = KneType.collectionElementVarType(elemType)
                appendLine("        val ${varName}Ptr = (_retPtr + $startOffset)!!.reinterpret<$varType>()")
                appendLine("        val $varName = List(_count) { ${varName}Ptr[it] }")
            }
        }
    }

    private fun fieldSize(type: KneType): Int = when (type) {
        KneType.INT, KneType.FLOAT, KneType.BOOLEAN -> 4
        KneType.LONG, KneType.DOUBLE -> 8
        KneType.BYTE -> 1
        KneType.SHORT -> 2
        is KneType.ENUM -> 4
        else -> 8
    }

    /** Allocate a native array for a collection element type in memScoped callback. */
    private fun StringBuilder.appendCollectionArrayAlloc(idx: Int, elemType: KneType, srcExpr: String, prefix: String) {
        val bufName = "_${prefix}Buf$idx"
        val varType = KneType.collectionElementVarType(elemType)
        when (elemType) {
            KneType.STRING -> {
                appendLine("        val $bufName = allocArray<ByteVar>($srcExpr.sumOf { it.encodeToByteArray().size + 1 }.coerceAtLeast(1))")
                appendLine("        var _${prefix}Off$idx = 0")
                appendLine("        for (_s in $srcExpr) { val _b = _s.encodeToByteArray(); _b.forEachIndexed { j, b -> $bufName[_${prefix}Off$idx + j] = b }; $bufName[_${prefix}Off$idx + _b.size] = 0; _${prefix}Off$idx += _b.size + 1 }")
            }
            KneType.BOOLEAN -> {
                appendLine("        val $bufName = allocArray<IntVar>($srcExpr.size)")
                appendLine("        $srcExpr.forEachIndexed { j, v -> $bufName[j] = if (v) 1 else 0 }")
            }
            is KneType.ENUM -> {
                appendLine("        val $bufName = allocArray<IntVar>($srcExpr.size)")
                appendLine("        $srcExpr.forEachIndexed { j, v -> $bufName[j] = v.ordinal }")
            }
            else -> {
                appendLine("        val $bufName = allocArray<$varType>($srcExpr.size)")
                appendLine("        $srcExpr.forEachIndexed { j, v -> $bufName[j] = v }")
            }
        }
    }

    /** Expand a type into its C ABI param type(s). DATA_CLASS becomes multiple field types. LIST/SET/MAP become pointer+size. */
    private fun expandCFuncParamTypes(type: KneType): List<String> = when (type) {
        KneType.BYTE_ARRAY -> listOf("CPointer<ByteVar>?", "Int") // ptr + size
        is KneType.DATA_CLASS -> type.fields.map { cFunctionParamType(it.type) }
        is KneType.LIST -> listOf(KneType.collectionPointerType(type.elementType).replace("?", "") + "?", "Int")
        is KneType.SET -> listOf(KneType.collectionPointerType(type.elementType).replace("?", "") + "?", "Int")
        is KneType.MAP -> listOf(
            KneType.collectionPointerType(type.keyType).replace("?", "") + "?",
            KneType.collectionPointerType(type.valueType).replace("?", "") + "?",
            "Int"
        )
        else -> listOf(cFunctionParamType(type))
    }

    /** Map a KneType to the corresponding C-ABI type for CFunction signatures. */
    private fun cFunctionParamType(type: KneType): String = when (type) {
        KneType.INT -> "Int"
        KneType.LONG -> "Long"
        KneType.DOUBLE -> "Double"
        KneType.FLOAT -> "Float"
        KneType.BOOLEAN -> "Int" // C uses int for bool
        KneType.BYTE -> "Byte"
        KneType.SHORT -> "Short"
        KneType.STRING -> "CPointer<ByteVar>?"
        is KneType.OBJECT -> "Long" // opaque StableRef handle
        is KneType.ENUM -> "Int" // ordinal
        else -> "Int" // fallback
    }

    private fun cFunctionReturnType(type: KneType): String = when (type) {
        KneType.UNIT -> "Unit"
        KneType.BOOLEAN -> "Int" // C uses int for bool
        KneType.STRING -> "CPointer<ByteVar>?"
        KneType.BYTE_ARRAY -> "CPointer<ByteVar>?" // packed buffer: [size][pad][data]
        is KneType.OBJECT -> "Long" // opaque StableRef handle
        is KneType.ENUM -> "Int"
        is KneType.DATA_CLASS -> "CPointer<ByteVar>?" // pointer to struct
        is KneType.LIST, is KneType.SET, is KneType.MAP -> "CPointer<ByteVar>?" // packed buffer
        else -> cFunctionParamType(type)
    }

    /** Emit the return statement for a given expression and type. */
    private fun StringBuilder.appendReturnStatement(expr: String, type: KneType) {
        when (type) {
            KneType.BYTE_ARRAY -> appendByteArrayReturn(expr)
            KneType.STRING -> appendStringReturn(expr)
            KneType.UNIT -> appendLine("    $expr")
            KneType.BOOLEAN -> appendLine("    return if ($expr) 1 else 0")
            is KneType.OBJECT, is KneType.INTERFACE -> appendLine("    return StableRef.create($expr).asCPointer().toLong()")
            is KneType.ENUM -> appendLine("    return ($expr).ordinal")
            is KneType.FUNCTION -> appendLine("    return StableRef.create($expr).asCPointer().toLong()")
            is KneType.NULLABLE -> appendNullableReturn(expr, type)
            else -> appendLine("    return $expr")
        }
    }

    /** Emit return statement for a nullable expression. */
    private fun StringBuilder.appendNullableReturn(expr: String, type: KneType.NULLABLE) {
        appendLine("    val _result = $expr")
        when (type.inner) {
            KneType.STRING -> {
                appendLine("    if (_result == null) return -1")
                appendLine("    val bytes = _result.encodeToByteArray()")
                appendLine("    val writeLen = minOf(bytes.size, outLen - 1)")
                appendLine("    bytes.forEachIndexed { i, b -> if (i < writeLen) outBuf?.set(i, b) }")
                appendLine("    outBuf?.set(writeLen, 0)")
                appendLine("    return bytes.size")
            }
            KneType.BOOLEAN -> appendLine("    return if (_result == null) -1 else if (_result) 1 else 0")
            KneType.INT -> appendLine("    return _result?.toLong() ?: Long.MIN_VALUE")
            KneType.LONG -> appendLine("    return _result ?: Long.MIN_VALUE")
            KneType.SHORT -> appendLine("    return _result?.toInt() ?: Int.MIN_VALUE")
            KneType.BYTE -> appendLine("    return _result?.toInt() ?: Int.MIN_VALUE")
            KneType.FLOAT -> appendLine("    return _result?.toRawBits()?.toLong() ?: Long.MIN_VALUE")
            KneType.DOUBLE -> appendLine("    return _result?.toRawBits() ?: Long.MIN_VALUE")
            is KneType.OBJECT, is KneType.INTERFACE -> appendLine("    return if (_result != null) StableRef.create(_result).asCPointer().toLong() else 0L")
            is KneType.ENUM -> appendLine("    return _result?.ordinal ?: -1")
            KneType.UNIT -> appendLine("    _result")
            else -> appendLine("    return _result")
        }
    }

    /** Emit the setter body for a property assignment. */
    private fun StringBuilder.appendSetterBody(target: String, type: KneType) {
        when (type) {
            KneType.STRING -> appendLine("    $target = value?.toKString() ?: \"\"")
            KneType.BOOLEAN -> appendLine("    $target = value != 0")
            is KneType.ENUM -> appendLine("    $target = ${type.fqName}.entries[value]")
            is KneType.OBJECT -> appendLine("    $target = value.toCPointer<COpaque>()!!.asStableRef<${type.fqName}>().get()")
            is KneType.INTERFACE -> appendLine("    $target = value.toCPointer<COpaque>()!!.asStableRef<${type.fqName}>().get()")
            is KneType.NULLABLE -> appendNullableSetterBody(target, type)
            else -> appendLine("    $target = value")
        }
    }

    private fun StringBuilder.appendNullableSetterBody(target: String, type: KneType.NULLABLE) {
        when (type.inner) {
            KneType.STRING -> appendLine("    $target = value?.toKString()")
            KneType.BOOLEAN -> appendLine("    $target = if (value >= 0) (value != 0) else null")
            KneType.INT -> appendLine("    $target = if (value != Long.MIN_VALUE) value.toInt() else null")
            KneType.LONG -> appendLine("    $target = if (value != Long.MIN_VALUE) value else null")
            KneType.SHORT -> appendLine("    $target = if (value != Int.MIN_VALUE) value.toShort() else null")
            KneType.BYTE -> appendLine("    $target = if (value != Int.MIN_VALUE) value.toByte() else null")
            KneType.FLOAT -> appendLine("    $target = if (value != Long.MIN_VALUE) Float.fromBits(value.toInt()) else null")
            KneType.DOUBLE -> appendLine("    $target = if (value != Long.MIN_VALUE) Double.fromBits(value) else null")
            is KneType.OBJECT -> appendLine("    $target = if (value != 0L) value.toCPointer<COpaque>()!!.asStableRef<${type.inner.fqName}>().get() else null")
            is KneType.ENUM -> appendLine("    $target = if (value >= 0) ${type.inner.fqName}.entries[value] else null")
            else -> appendLine("    $target = value")
        }
    }

    private fun StringBuilder.appendStringReturn(expr: String) {
        appendLine("    val result = $expr")
        appendLine("    val bytes = result.encodeToByteArray()")
        appendLine("    val writeLen = minOf(bytes.size, outLen - 1)")
        appendLine("    bytes.forEachIndexed { i, b -> if (i < writeLen) outBuf?.set(i, b) }")
        appendLine("    outBuf?.set(writeLen, 0)")
        appendLine("    return bytes.size")
    }

    private fun StringBuilder.appendByteArrayReturn(expr: String) {
        appendLine("    val result = $expr")
        appendLine("    val writeLen = minOf(result.size, outLen)")
        appendLine("    result.forEachIndexed { i, b -> if (i < writeLen) outBuf?.set(i, b) }")
        appendLine("    return result.size")
    }

