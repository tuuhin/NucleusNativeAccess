        }
        module.functions.forEach { fn -> fn.params.forEach { scanType(it.type) }; scanType(fn.returnType) }
        return result
    }

    /** Generate wrap bridges that create StableRef from buffer data (for DC param collection fields). */
    private fun StringBuilder.appendDcFieldCollectionWrapBridges(prefix: String, types: Set<KneType>) {
        val elemKeys = mutableSetOf<String>()
        val mapCombinations = mutableSetOf<Pair<String, String>>()
        for (type in types) {
            when (type) {
                is KneType.LIST -> if (type.elementType !is KneType.DATA_CLASS) elemKeys.add(suspendCollElemKey(type.elementType))
                is KneType.SET -> if (type.elementType !is KneType.DATA_CLASS) elemKeys.add(suspendCollElemKey(type.elementType))
                is KneType.MAP -> mapCombinations.add(Pair(suspendCollElemKey(type.keyType), suspendCollElemKey(type.valueType)))
                else -> {}
            }
        }
        for (key in elemKeys) {
            appendWrapCollBridgeForKey(prefix, key)
        }
        for ((kk, vk) in mapCombinations) {
            appendWrapMapBridge(prefix, kk, vk)
        }
    }

    /** Generate a single wrap bridge for creating a List/Set StableRef from buffer data. */
    private fun StringBuilder.appendWrapCollBridgeForKey(prefix: String, key: String) {
        val symbolName = "${prefix}_kne_wrapColl$key"
        when (key) {
            "String" -> {
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(buf: CPointer<ByteVar>?, count: Int): Long {")
                appendLine("    val _list = mutableListOf<String>()")
                appendLine("    var _offset = 0")
                appendLine("    for (i in 0 until count) {")
                appendLine("        val _s = (buf!! + _offset)!!.toKString()")
                appendLine("        _list.add(_s)")
                appendLine("        _offset += _s.encodeToByteArray().size + 1")
                appendLine("    }")
                appendLine("    return StableRef.create(_list as Any).asCPointer().toLong()")
                appendLine("}")
                appendLine()
            }
            "ObjHandle" -> {
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(buf: CPointer<LongVar>?, count: Int): Long {")
                appendLine("    val _list = List(count) { buf!![it].toCPointer<COpaque>()!!.asStableRef<Any>().get() }")
                appendLine("    return StableRef.create(_list as Any).asCPointer().toLong()")
                appendLine("}")
                appendLine()
            }
            "ByteArray" -> {
                // Single ByteArray → StableRef (takes raw ptr + size)
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(buf: CPointer<ByteVar>?, size: Int): Long {")
                appendLine("    val _ba = ByteArray(size) { buf!![it] }")
                appendLine("    return StableRef.create(_ba).asCPointer().toLong()")
                appendLine("}")
                appendLine()
            }
            "NestedColl" -> {
                // Each element is a Long handle (StableRef of inner collection)
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(buf: CPointer<LongVar>?, count: Int): Long {")
                appendLine("    val _list = List(count) {")
                appendLine("        val _ref = buf!![it].toCPointer<COpaque>()!!.asStableRef<Any>()")
                appendLine("        val _inner = _ref.get(); _ref.dispose(); _inner")
                appendLine("    }")
                appendLine("    return StableRef.create(_list as Any).asCPointer().toLong()")
                appendLine("}")
                appendLine()
            }
            else -> {
                val nativeType = when (key) {
                    "Long" -> "LongVar"
                    "Double" -> "DoubleVar"
                    "Float" -> "FloatVar"
                    else -> "IntVar"
                }
                val readExpr = when (key) {
                    "Boolean" -> "buf!![it] != 0"
                    "Enum" -> "buf!![it]" // ordinal stored, actual enum resolved on native side
                    "Short" -> "buf!![it].toShort()"
                    "Byte" -> "buf!![it].toByte()"
                    else -> "buf!![it]"
                }
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(buf: CPointer<$nativeType>?, count: Int): Long {")
                appendLine("    val _list = List(count) { $readExpr }")
                appendLine("    return StableRef.create(_list as Any).asCPointer().toLong()")
                appendLine("}")
                appendLine()
            }
        }
    }

    /** Generate a wrap bridge for Map StableRef creation. */
    private fun StringBuilder.appendWrapMapBridge(prefix: String, keyKey: String, valKey: String) {
        val symbolName = "${prefix}_kne_wrapMap${keyKey}${valKey}"
        val keyNativeType = if (keyKey == "String") "ByteVar" else "IntVar"
        val valNativeType = when (valKey) {
            "String" -> "ByteVar"
            "Long" -> "LongVar"
            "Double" -> "DoubleVar"
            "Float" -> "FloatVar"
            else -> "IntVar"
        }
        val keyParamStr = if (keyKey == "String") "keyBuf: CPointer<ByteVar>?, keyCount: Int" else "keyBuf: CPointer<$keyNativeType>?, count: Int"
        val valParamStr = if (valKey == "String") "valBuf: CPointer<ByteVar>?, valCount: Int" else "valBuf: CPointer<$valNativeType>?, valCount: Int"

        appendLine("@CName(\"$symbolName\")")
        appendLine("fun `$symbolName`($keyParamStr, $valParamStr): Long {")

        // Read keys
        if (keyKey == "String") {
            appendLine("    val _keys = mutableListOf<String>()")
            appendLine("    var _kOff = 0")
            appendLine("    for (i in 0 until keyCount) { val _s = (keyBuf!! + _kOff)!!.toKString(); _keys.add(_s); _kOff += _s.encodeToByteArray().size + 1 }")
        } else {
            appendLine("    val _keys = List(count) { keyBuf!![it] }")
        }

        // Read values
        if (valKey == "String") {
            appendLine("    val _vals = mutableListOf<String>()")
            appendLine("    var _vOff = 0")
            appendLine("    for (i in 0 until valCount) { val _s = (valBuf!! + _vOff)!!.toKString(); _vals.add(_s); _vOff += _s.encodeToByteArray().size + 1 }")
        } else {
            val readExpr = when (valKey) { "Long" -> "valBuf!![it]"; "Double" -> "valBuf!![it]"; "Float" -> "valBuf!![it]"; else -> "valBuf!![it]" }
            appendLine("    val _vals = List(valCount) { $readExpr }")
        }

        appendLine("    return StableRef.create(_keys.zip(_vals).toMap() as Any).asCPointer().toLong()")
        appendLine("}")
        appendLine()
    }

    /** Determine the element layout key for a collection element type. */
    private fun suspendCollElemKey(type: KneType): String = when (type) {
        KneType.INT -> "Int"
        KneType.LONG -> "Long"
        KneType.DOUBLE -> "Double"
        KneType.FLOAT -> "Float"
        KneType.SHORT -> "Short"
        KneType.BYTE -> "Byte"
        KneType.BOOLEAN -> "Boolean"
        KneType.STRING -> "String"
        is KneType.ENUM -> "Enum"
        is KneType.OBJECT -> "ObjHandle"
        KneType.BYTE_ARRAY -> "ByteArray"
        is KneType.LIST, is KneType.SET, is KneType.MAP -> "NestedColl"
        else -> "Int" // fallback
    }

    /** Generate suspend collection reader bridges for all used element types. */
    private fun StringBuilder.appendSuspendCollectionReaderBridges(prefix: String, types: Set<KneType>) {
        // Collect unique element layout keys needed (excluding DataClass — handled by list_DC bridges)
        val elemKeys = mutableSetOf<String>()
        val mapCombinations = mutableSetOf<Pair<String, String>>()
        for (type in types) {
            when (type) {
                is KneType.LIST -> {
                    if (type.elementType !is KneType.DATA_CLASS) elemKeys.add(suspendCollElemKey(type.elementType))
                }
                is KneType.SET -> {
                    if (type.elementType !is KneType.DATA_CLASS) elemKeys.add(suspendCollElemKey(type.elementType))
                }
                is KneType.MAP -> mapCombinations.add(Pair(suspendCollElemKey(type.keyType), suspendCollElemKey(type.valueType)))
                else -> {}
            }
        }

        // Generate per-element-key list/set reader bridges
        for (key in elemKeys) {
            appendSuspendCollReaderForKey(prefix, key)
        }

        // Generate per-combination map reader bridges
        for ((kk, vk) in mapCombinations) {
            appendSuspendMapReader(prefix, kk, vk)
        }
    }

    /** Generate a single suspend collection reader bridge for a given element key. */
    private fun StringBuilder.appendSuspendCollReaderForKey(prefix: String, key: String) {
        val symbolName = "${prefix}_kne_suspend_readColl$key"
        when (key) {
            "String" -> {
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(handle: Long, outBuf: CPointer<ByteVar>?, outBufLen: Int): Int {")
                appendLine("    val _coll = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get()")
                appendLine("    val _list = when (_coll) { is List<*> -> _coll; is Set<*> -> _coll.toList(); else -> emptyList() }")
                appendLine("    var _offset = 0")
                appendLine("    for (_s in _list) {")
                appendLine("        val _bytes = (_s as String).encodeToByteArray()")
                appendLine("        if (_offset + _bytes.size + 1 > outBufLen) break")
                appendLine("        _bytes.forEachIndexed { i, b -> outBuf?.set(_offset + i, b) }")
                appendLine("        outBuf?.set(_offset + _bytes.size, 0)")
                appendLine("        _offset += _bytes.size + 1")
                appendLine("    }")
                appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
                appendLine("    return _list.size")
                appendLine("}")
                appendLine()
            }
            "ObjHandle" -> {
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(handle: Long, outBuf: CPointer<LongVar>?, outLen: Int): Int {")
                appendLine("    val _coll = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get()")
                appendLine("    val _list = when (_coll) { is List<*> -> _coll; is Set<*> -> _coll.toList(); else -> emptyList() }")
                appendLine("    val _writeLen = minOf(_list.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create(_list[i]!!).asCPointer().toLong()")
                appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
                appendLine("    return _list.size")
                appendLine("}")
                appendLine()
            }
            "ByteArray" -> {
                // Each ByteArray element wrapped as StableRef handle (Long)
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(handle: Long, outBuf: CPointer<LongVar>?, outLen: Int): Int {")
                appendLine("    val _coll = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get()")
                appendLine("    val _list = when (_coll) { is List<*> -> _coll; is Set<*> -> _coll.toList(); else -> emptyList() }")
                appendLine("    val _writeLen = minOf(_list.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create(_list[i] as ByteArray).asCPointer().toLong()")
                appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
                appendLine("    return _list.size")
                appendLine("}")
                appendLine()
            }
            "NestedColl" -> {
                // Each nested collection element wrapped as StableRef handle (Long)
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(handle: Long, outBuf: CPointer<LongVar>?, outLen: Int): Int {")
                appendLine("    val _coll = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get()")
                appendLine("    val _list = when (_coll) { is List<*> -> _coll; is Set<*> -> _coll.toList(); else -> emptyList() }")
                appendLine("    val _writeLen = minOf(_list.size, outLen)")
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = StableRef.create(_list[i] as Any).asCPointer().toLong()")
                appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
                appendLine("    return _list.size")
                appendLine("}")
                appendLine()
            }
            else -> {
                // Int, Long, Double, Float, Short, Byte, Boolean, Enum — all use the same pattern
                val nativeType = when (key) {
                    "Long" -> "LongVar"
                    "Double" -> "DoubleVar"
                    "Float" -> "FloatVar"
                    else -> "IntVar" // Int, Short, Byte, Boolean, Enum
                }
                appendLine("@CName(\"$symbolName\")")
                appendLine("fun `$symbolName`(handle: Long, outBuf: CPointer<$nativeType>?, outLen: Int): Int {")
                appendLine("    val _coll = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get()")
                appendLine("    val _list = when (_coll) { is List<*> -> _coll; is Set<*> -> _coll.toList(); else -> emptyList() }")
                appendLine("    val _writeLen = minOf(_list.size, outLen)")
                val writeExpr = when (key) {
                    "Boolean" -> "if (_list[i] as Boolean) 1 else 0"
                    "Enum" -> "(_list[i] as Enum<*>).ordinal"
                    "Short" -> "(_list[i] as Short).toInt()"
                    "Byte" -> "(_list[i] as Byte).toInt()"
                    "Long" -> "_list[i] as Long"
                    "Double" -> "_list[i] as Double"
                    "Float" -> "_list[i] as Float"
                    else -> "_list[i] as Int"
                }
                appendLine("    for (i in 0 until _writeLen) outBuf!![i] = $writeExpr")
                appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
                appendLine("    return _list.size")
                appendLine("}")
                appendLine()
            }
        }
    }

    /** Generate a suspend map reader bridge for a given (keyKey, valueKey) combination. */
    private fun StringBuilder.appendSuspendMapReader(prefix: String, keyKey: String, valKey: String) {
        val symbolName = "${prefix}_kne_suspend_readMap${keyKey}${valKey}"

        // Determine native pointer types for keys and values
        val keyNativeType = if (keyKey == "String") "ByteVar" else "IntVar"
        val valNativeType = when (valKey) {
            "String" -> "ByteVar"
            "Long" -> "LongVar"
            "Double" -> "DoubleVar"
            "Float" -> "FloatVar"
            else -> "IntVar"
        }

        val keyParamStr = if (keyKey == "String") {
            "outKeys: CPointer<ByteVar>?, outKeysLen: Int"
        } else {
            "outKeys: CPointer<$keyNativeType>?, outKeysMaxLen: Int"
        }
        val valParamStr = if (valKey == "String") {
            "outVals: CPointer<ByteVar>?, outValsLen: Int"
        } else {
            "outVals: CPointer<$valNativeType>?, outValsMaxLen: Int"
        }

        appendLine("@CName(\"$symbolName\")")
        appendLine("fun `$symbolName`(handle: Long, $keyParamStr, $valParamStr): Int {")
        appendLine("    val _map = handle.toCPointer<COpaque>()!!.asStableRef<Any>().get() as Map<*, *>")
        appendLine("    val _keys = _map.keys.toList()")
        appendLine("    val _vals = _map.values.toList()")

        // Write keys
        if (keyKey == "String") {
            appendLine("    var _kOff = 0")
            appendLine("    for (_k in _keys) {")
            appendLine("        val _bytes = (_k as String).encodeToByteArray()")
            appendLine("        if (_kOff + _bytes.size + 1 > outKeysLen) break")
            appendLine("        _bytes.forEachIndexed { i, b -> outKeys?.set(_kOff + i, b) }")
            appendLine("        outKeys?.set(_kOff + _bytes.size, 0)")
            appendLine("        _kOff += _bytes.size + 1")
            appendLine("    }")
        } else {
            appendLine("    val _kWriteLen = minOf(_keys.size, outKeysMaxLen)")
            appendLine("    for (i in 0 until _kWriteLen) outKeys!![i] = _keys[i] as Int")
        }

        // Write values
        if (valKey == "String") {
            appendLine("    var _vOff = 0")
            appendLine("    for (_v in _vals) {")
            appendLine("        val _bytes = (_v as String).encodeToByteArray()")
            appendLine("        if (_vOff + _bytes.size + 1 > outValsLen) break")
            appendLine("        _bytes.forEachIndexed { i, b -> outVals?.set(_vOff + i, b) }")
            appendLine("        outVals?.set(_vOff + _bytes.size, 0)")
            appendLine("        _vOff += _bytes.size + 1")
            appendLine("    }")
        } else {
            val valWriteExpr = when (valKey) {
                "Long" -> "_vals[i] as Long"
                "Double" -> "_vals[i] as Double"
                "Float" -> "_vals[i] as Float"
                "Enum" -> "(_vals[i] as Enum<*>).ordinal"
                "Boolean" -> "if (_vals[i] as Boolean) 1 else 0"
                else -> "_vals[i] as Int"
            }
            appendLine("    val _vWriteLen = minOf(_vals.size, outValsMaxLen)")
            appendLine("    for (i in 0 until _vWriteLen) outVals!![i] = $valWriteExpr")
        }

        appendLine("    handle.toCPointer<COpaque>()!!.asStableRef<Any>().dispose()")
        appendLine("    return _map.size")
        appendLine("}")
        appendLine()
    }

